#include "3-calc.h"
/**
 * op_add - sum function
 * @a: first number
 * @b: second number
 * Return: sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - submission function
 * @a: first number
 * @b: second number
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: first number
 * @b: second number
 * Return: mutiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divison function
 * @a: first number
 * @b: second number
 * Return: divison
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder function
 * @a: first number
 * @b: second number
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
